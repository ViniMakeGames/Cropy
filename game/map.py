def draw_map(surface, tilesize):
    for y, row in enumerate(MAP_LAYOUT):
        for x, tile in enumerate(row):
            rect_outline = pygame.Rect(x * tilesize, y * tilesize, tilesize, tilesize)
            rect = pygame.Rect(1 + x * tilesize, 1 + y * tilesize, tilesize - 1, tilesize - 1)
            pygame.draw.rect(surface, (0, 0, 0), rect_outline)
            if tile == 0:
                pygame.draw.rect(surface, (34, 177, 76), rect)  # grass
            elif tile == 1:
                pygame.draw.rect(surface, (185, 122, 87), rect)  # dirt

def get_collision_tiles(tilesize):
    tiles = []
    for y, row in enumerate(MAP_LAYOUT):
        for x, tile in enumerate(row):
            if tile in BLOCKED_TILES:
                rect = pygame.Rect(x * tilesize, y * tilesize, tilesize, tilesize)
                tiles.append(rect)
    return tiles